      La mente &#232; una sorta di &quot;black box&quot; il cui funzionamento interno &#232; inconoscibile e, per certi aspetti, irrilevante.
    </p>
    <p>
      L'oggetto dell'indagine dei comportamentisti &#232; la relazione tra gli stimoli ambientali e le risposte comportamentali.
    </p>
    <p>
      Il meccanismo del condizionamento, cio&#232; l'associazione ripetuta di uno stimolo con una risposta ad esso non direttamente correlata, &#232; studiato e approfondito dai comportamentisti.
